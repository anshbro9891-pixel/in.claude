/** Create a new sandbox project */
export function createSandboxProject(name: string, template: string): SandboxProject {
  const tmpl = PROJECT_TEMPLATES[template] || PROJECT_TEMPLATES["static-html"];

  return {
    id: `proj_${Date.now()}_${Math.random().toString(36).slice(2, 8)}`,
    name,
    template,
    files: tmpl.files.map((f) => ({ ...f })),
    status: "idle",
    createdAt: Date.now(),
  };
}

/** Update a file in the project */
export function updateProjectFile(project: SandboxProject, path: string, content: string): SandboxProject {
  const existing = project.files.findIndex((f) => f.path === path);
  if (existing >= 0) {
    project.files[existing] = { ...project.files[existing], content };
  } else {
    project.files.push({ path, content });
  }
  return { ...project };
}

/** Delete a file from the project */
export function deleteProjectFile(project: SandboxProject, path: string): SandboxProject {
  project.files = project.files.filter((f) => f.path !== path);
  return { ...project };
}
