/**
 * INCLAW Code Sandbox
 *
 * Provides safe code execution environment for testing generated code.
 * Uses WebContainer API concepts for browser-based execution
 * and server-side Docker sandboxing for production.
 */

export interface SandboxFile {
  path: string;
  content: string;
  language?: string;
}

export interface SandboxProject {
  id: string;
  name: string;
  template: string;
  files: SandboxFile[];
  status: "idle" | "running" | "building" | "error";
  previewUrl?: string;
  createdAt: number;
}

export interface ExecutionResult {
  stdout: string;
  stderr: string;
  exitCode: number;
  duration: number;
  output?: string;
}

export interface PreviewConfig {
  port: number;
  command: string;
  env?: Record<string, string>;
}

